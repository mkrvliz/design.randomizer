const shown = new Set();

app.get('/api/theme', (req, res) => {
  if (shown.size === themes.length) shown.clear();

  const remaining = themes.filter((_, i) => !shown.has(i));
  const idx = themes.indexOf(remaining[Math.floor(Math.random() * remaining.length)]);
  shown.add(idx);

  res.json(themes[idx]);
});

